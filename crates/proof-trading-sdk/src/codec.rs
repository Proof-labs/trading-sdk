/// Byte buffer that always serializes as msgpack `bin` (0xc4/c5/c6),
/// never as msgpack `array` (0xdc/0xdd). Used in `WireTxEnvelope` fields
/// that must match the Go CheckTx parser's wire format.
#[derive(Clone, Debug)]
struct WireBlob(Vec<u8>);

impl Serialize for WireBlob {
    fn serialize<S: Serializer>(&self, s: S) -> Result<S::Ok, S::Error> {
        serde_bytes::Bytes::new(&self.0).serialize(s)
    }
}

impl<'de> Deserialize<'de> for WireBlob {
    fn deserialize<D: Deserializer<'de>>(d: D) -> Result<Self, D::Error> {
        serde_bytes::ByteBuf::deserialize(d).map(|b| WireBlob(b.into_vec()))
    }
}

#[cfg(test)]
impl WireBlob {
    fn from_test_payload(bytes: Vec<u8>) -> Self {
        WireBlob(bytes)
    }
}

impl std::ops::Deref for WireBlob {
    type Target = Vec<u8>;

    fn deref(&self) -> &Vec<u8> {
        &self.0
    }
}

/// Envelope version emitted by this build.
pub const ENVELOPE_VERSION: u8 = 2;

/// Wire envelope versions this build accepts on decode. Single source of
/// truth — mirrored by `supportedEnvelopeVersions` in
/// `exchange-node/check_tx.go` and the SDK check in `sdk/src/codec.ts`.
/// Add a new version here when introducing one; drop an old one when it
/// is no longer permitted. Decoders MUST consult this slice instead of
/// hardcoding numeric comparisons.
pub const SUPPORTED_ENVELOPE_VERSIONS: &[u8] = &[ENVELOPE_VERSION];

/// Wire envelope: `[version=2, action_type, seq, payload_bytes, pubkey, signature]`.
///
/// Byte fields use [`WireBlob`] which enforces msgpack `bin` encoding at the
/// type level (not just an annotation). The Go CheckTx parser
/// (exchange-node/check_tx.go) and the SDK both expect this shape.
///
/// The envelope `version` byte stays `2` (exchange-core accepts only `[2]`).
/// This is independent of the signing **domain prefix**, which is
/// `"ProofExchange-v3"` (see `crypto.rs`) — bumped to v3 when chain_id
/// binding landed. The legacy unsigned (v1) envelope was removed pre-launch.
#[derive(Serialize, Deserialize)]
struct WireTxEnvelope {
    version: u8,
    action_type: u8,
    seq: u64,
    payload: WireBlob,
    #[serde(with = "serde_bytes")]
    pubkey: [u8; 32],
    #[serde(with = "serde_bytes")]
    signature: [u8; 64],
}

/// Authentication data extracted from the wire envelope.
#[derive(Debug)]
pub struct TxAuth {
    pub pubkey: [u8; 32],
    pub signature: [u8; 64],
    pub action_type: u8,
    pub payload: Vec<u8>,
}

/// Result of decoding a transaction.
#[derive(Debug)]
pub struct DecodedTx {
    pub action: Action,
    pub seq: u64,
    pub auth: TxAuth,
}

/// Decode raw tx bytes as a signed wire envelope:
/// fixarray(6) `[version=2, action_type, seq, payload, pubkey, signature]`.
pub fn decode_tx(bytes: &[u8]) -> Result<DecodedTx, ExecError> {
    if bytes.is_empty() {
        return Err(ExecError::DecodeError("empty tx".to_string()));
    }

    let envelope: WireTxEnvelope =
        rmp_serde::from_slice(bytes).map_err(|e| ExecError::DecodeError(e.to_string()))?;

    if !SUPPORTED_ENVELOPE_VERSIONS.contains(&envelope.version) {
        return Err(ExecError::DecodeError(format!(
            "unsupported envelope version: {} (supported: {:?})",
            envelope.version, SUPPORTED_ENVELOPE_VERSIONS
        )));
    }

    let action = decode_action(envelope.action_type, &envelope.payload)?;

    Ok(DecodedTx {
        action,
        seq: envelope.seq,
        auth: TxAuth {
            pubkey: envelope.pubkey,
            signature: envelope.signature,
            action_type: envelope.action_type,
            payload: envelope.payload.0,
        },
    })
}

/// Encode an action with a pre-computed pubkey + signature into a wire envelope.
/// Most callers should use [`sign_and_encode`] instead; this exists for paths
/// (api-gateway forward, FFI relays) that already hold raw signature bytes.
pub fn encode_signed_tx(
    action: &Action,
    seq: u64,
    pubkey: &[u8; 32],
    signature: &[u8; 64],
) -> Result<Vec<u8>, ExecError> {
    let EncodedAction {
        action_type,
        payload,
    } = action.encode_action()?;
    let envelope = WireTxEnvelope {
        version: ENVELOPE_VERSION,
        action_type,
        seq,
        payload,
        pubkey: *pubkey,
        signature: *signature,
    };
    rmp_serde::to_vec(&envelope).map_err(|e| ExecError::InternalError(e.to_string()))
}

/// Sign an action and encode it as a wire envelope, binding the
/// signature to a specific `chain_id`. The signing bytes carry a 32-byte
/// chain_id prefix per audit B4 — `chain_id` is established by
/// genesis / snapshot-load, not carried on the wire.
pub fn sign_and_encode_with_chain(
    chain_id: &[u8; 32],
    action: &Action,
    seq: u64,
    signing_key: &ed25519_dalek::SigningKey,
) -> Result<Vec<u8>, ExecError> {
    use ed25519_dalek::Signer;

    let EncodedAction {
        action_type,
        payload,
    } = action.encode_action()?;
    let msg = crate::crypto::signing_message(chain_id, action_type, seq, &payload);
    let sig = signing_key.sign(&msg);

    let pubkey = signing_key.verifying_key().to_bytes();
    let signature = sig.to_bytes();

    let envelope = WireTxEnvelope {
        version: ENVELOPE_VERSION,
        action_type,
        seq,
        payload,
        pubkey,
        signature,
    };
    rmp_serde::to_vec(&envelope).map_err(|e| ExecError::InternalError(e.to_string()))
}

/// Test-only convenience: sign with the `UNBOUND_CHAIN_ID`. Production
/// code MUST use `sign_and_encode_with_chain` with a real chain_id,
/// otherwise the signature is trivially replayable on any
/// zero-chain_id deployment. Kept public because `exchange-core`
/// integration tests are spread across multiple modules.
pub fn sign_and_encode(
    action: &Action,
    seq: u64,
    signing_key: &ed25519_dalek::SigningKey,
) -> Result<Vec<u8>, ExecError> {
    sign_and_encode_with_chain(&crate::crypto::UNBOUND_CHAIN_ID, action, seq, signing_key)
}

/// Sign a raw payload (bytes + action_type) and encode it as a wire envelope.
/// Used by PyO3 / WASM bindings that hold pre-encoded payload bytes.
pub fn sign_and_encode_payload(
    chain_id: &[u8; 32],
    action_type: u8,
    payload: &[u8],
    seq: u64,
    signing_key: &ed25519_dalek::SigningKey,
) -> Result<Vec<u8>, ExecError> {
    use ed25519_dalek::Signer;

    let msg = crate::crypto::signing_message(chain_id, action_type, seq, payload);
    let sig = signing_key.sign(&msg);
    let pubkey = signing_key.verifying_key().to_bytes();
    let signature = sig.to_bytes();

    let envelope = WireTxEnvelope {
        version: ENVELOPE_VERSION,
        action_type,
        seq,
        payload: WireBlob(payload.to_vec()),
        pubkey,
        signature,
    };
    rmp_serde::to_vec(&envelope).map_err(|e| ExecError::InternalError(e.to_string()))
}

