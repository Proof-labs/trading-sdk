    #[test]
    fn decode_legacy_create_market_8field_now_rejected() {
        // Pre-pool_id wire shape: 8-field positional msgpack array.
        // Although `pool_id` is `serde(default)`, the trailing `sz_decimals`
        // and `ticker` are MANDATORY (no default) and positionally follow
        // `pool_id`, so an 8-field array cannot satisfy the struct and is
        // rejected at decode — matching the engine. Old SDK builds that
        // pre-date sz_decimals can no longer create markets, by design.
        //
        // Field order matches the struct definition in `types.rs`:
        //   market, im_bps, mm_bps, taker_fee_bps, maker_fee_bps,
        //   signer, funding_interval_ms, max_funding_rate_bps,
        //   [pool_id intentionally omitted]
        let payload_bytes = rmp_serde::to_vec(&(
            99u32,        // market
            500u32,       // im_bps
            250u32,       // mm_bps
            5u32,         // taker_fee_bps
            2u32,         // maker_fee_bps
            [0xAAu8; 20], // signer
            60_000u64,    // funding_interval_ms
            3000u32,      // max_funding_rate_bps
                          // intentionally NO pool_id (legacy SDK)
        ))
        .unwrap();

        // Envelope uses WireBlob for payload, which enforces msgpack
        // bin encoding at the type level. The pubkey/signature are zero
        // because this test only exercises the codec's payload-decode
        // path, not signature verification.
        let envelope = WireTxEnvelope {
            version: 2,
            action_type: CreateMarket::ACTION_TYPE,
            seq: 0,
            payload: WireBlob::from_test_payload(payload_bytes),
            pubkey: [0u8; 32],
            signature: [0u8; 64],
        };
        let encoded = rmp_serde::to_vec(&envelope).unwrap();

        assert!(
            decode_tx(&encoded).is_err(),
            "legacy 8-field CreateMarket must now be rejected: sz_decimals/ticker are mandatory"
        );
    }

