use crate::types::*;
use serde::{de::DeserializeOwned, Deserialize, Deserializer, Serialize, Serializer};

/// Structured output of encoding an action payload for the wire.
struct EncodedAction {
    action_type: u8,
    payload: WireBlob,
}

/// Trait that pairs each action payload type with its wire discriminant
/// and provides msgpack encode/decode.
trait EncodePayload: Serialize + DeserializeOwned {
    const ACTION_TYPE: u8;

    fn encode_payload(&self) -> Result<WireBlob, ExecError> {
        let bytes = rmp_serde::to_vec(self).map_err(|e| ExecError::InternalError(e.to_string()))?;
        Ok(WireBlob(bytes))
    }

    fn encode_action(&self) -> Result<EncodedAction, ExecError> {
        self.encode_payload().map(|payload| EncodedAction {
            action_type: Self::ACTION_TYPE,
            payload,
        })
    }
}

macro_rules! impl_action_encoding {
    ($($ty:ident => $code:expr),+ $(,)?) => {
        /// Top-level transaction action enum. MessagePack tag selects the
        /// variant. Each variant wraps the corresponding payload struct.
        ///
        /// Every variant here has an `ACTION_TYPE` constant (generated by the
        /// same macro) and appears in `get_action_types()` — the list is the
        /// single source of truth for encode, decode, and the name↔code map.
        #[derive(Clone, Debug, Serialize, Deserialize, derive_more::From)]
        pub enum Action {
            $(
                #[doc = concat!("See [`", stringify!($ty), "`].")]
                $ty($ty),
            )+
        }

        $(impl EncodePayload for $ty {
            const ACTION_TYPE: u8 = $code;
        })+

        impl Action {
            fn encode_action(&self) -> Result<EncodedAction, ExecError> {
                match self {
                    $(Self::$ty(cmd) => cmd.encode_action()),+,
                }
            }
        }

        fn decode_action(action_type: u8, payload: &[u8]) -> Result<Action, ExecError> {
            let de = |e: rmp_serde::decode::Error| ExecError::DecodeError(e.to_string());
            match action_type {
                $($code => rmp_serde::from_slice::<$ty>(payload).map_err(de).map(Action::from)),+,
                other => Err(ExecError::DecodeError(format!(
                    "unknown action_type: {other:#x}"
                ))),
            }
        }

        /// Deserialize a structured action payload (from any self-describing
        /// source — `pythonize`, `serde_wasm_bindgen`, JSON …) into the typed
        /// payload struct for `action_type`, then encode it to the exact
        /// MessagePack wire bytes via the same `rmp-serde` path the engine
        /// uses. This is the single bridge entry point that lets native
        /// bindings hand the core *field values* and get authoritative
        /// payload bytes back — no per-language codec, no field-order drift.
        ///
        /// Generated by the same macro as encode/decode so a new action is
        /// covered automatically.
        pub fn encode_payload_dyn<'de, D: serde::Deserializer<'de>>(
            action_type: u8,
            de: D,
        ) -> Result<Vec<u8>, ExecError> {
            match action_type {
                $($code => {
                    let value = <$ty as serde::Deserialize>::deserialize(de)
                        .map_err(|e| ExecError::DecodeError(e.to_string()))?;
                    value.encode_payload().map(|b| b.0)
                })+,
                other => Err(ExecError::DecodeError(format!(
                    "unknown action_type: {other:#x}"
                ))),
            }
        }

        /// Decode raw MessagePack payload bytes for `action_type` into the
        /// typed payload struct, then re-serialize into the provided
        /// `Serializer` (e.g. `pythonize`'s `Pythonizer`). The inverse of
        /// [`encode_payload_dyn`]; lets a binding hand back a native object
        /// (Python dict, JS object) without reimplementing field order.
        pub fn decode_payload_dyn<S: serde::Serializer>(
            action_type: u8,
            payload: &[u8],
            ser: S,
        ) -> Result<S::Ok, ExecError> {
            let de = |e: rmp_serde::decode::Error| ExecError::DecodeError(e.to_string());
            match action_type {
                $($code => {
                    let value = rmp_serde::from_slice::<$ty>(payload).map_err(de)?;
                    value
                        .serialize(ser)
                        .map_err(|e| ExecError::InternalError(e.to_string()))
                })+,
                other => Err(ExecError::DecodeError(format!(
                    "unknown action_type: {other:#x}"
                ))),
            }
        }

        /// Return all action-type name → code mappings. Generated by the same
        /// macro that defines encoding — adding a new action here automatically
        /// adds it to the manifest.
        pub fn get_action_types() -> Vec<(&'static str, u8)> {
            fn entry<A: EncodePayload>(name: &'static str) -> (&'static str, u8) {
                (name, A::ACTION_TYPE)
            }
            vec![$(
                entry::<$ty>(stringify!($ty)),
            )+]
        }
    }
}

